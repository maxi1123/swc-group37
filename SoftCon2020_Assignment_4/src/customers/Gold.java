package customers;

public class Gold extends Customer{

    public Gold(String name,
                   String surname,
                   int age,
                   int account_number,
                   int savings,
                   int ID){

        this.name = name;
        this.surname = surname;
        this.age = age;
        this.account_number = account_number;
        this.savings = savings;
        this.ID = ID;
    }

}
