package Employees;

public class Employee {
    String name;
    String surname;
    String address;
    long phone;
    String ID;

    Employee(String name,String surname,String address,long phone,String ID){
        this.name = name;
        this.surname = surname;
        this.address = address;
        this.phone = phone;
        this.ID = ID;

    }
}
